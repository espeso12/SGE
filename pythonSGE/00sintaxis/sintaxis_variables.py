decimal = 3.14  # Número decimal
booleano = True  # Booleano (Verdadero/Falso)

# Imprimir el contenido de las variables
print(texto)
print(entero)
print(decimal)
print(booleano)

# Imprimir el tipo de cada variable
print(type(texto))
print(type(entero))
print(type(decimal))
print(type(booleano))
