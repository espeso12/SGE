# Ejemplos de listas
lista = [3, 1, 4, 1, 5, 9]

# Inserción
print("Después de añadir 9:", lista)

# Borrado
lista.remove(1)  # Elimina la primera aparición de 1
print("Después de eliminar 1:", lista)

# Actualización
lista[0] = 10
print("Después de actualizar el primer elemento:", lista)

# Ordenación
lista.sort()
print("Lista ordenada:", lista)
